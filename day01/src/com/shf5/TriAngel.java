package com.shf5;

public class TriAngel {
  private double base;
  private double height;
  public TriAngel(){}
  public TriAngel(double b,double h){
    base=b;
    height=h;
  }
  public void setBase(double b){
    base=b;
  }
  public double getBase(){
    return base;
  }
  public void setHeight(double h){
    height=h;
  }
  public double getHeight(){
    return height;
  }
}
