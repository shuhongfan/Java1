public class VaruableDemo01 {
    public static void main(String[] args){
        int a = 10;
        System.out.println(a);
        a=20;
        System.out.println(a);

        long l=10000000000L;
        System.out.println(l);

        float f=13.14f;
        System.out.println(f);

        double d=10;
        System.out.println(d);

        char c='b';
        System.out.println(c);

        int k=(int)88.88;
        System.out.println(k);
    }
}
